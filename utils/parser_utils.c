#include "utils.h"

int	is_special(char c)
{
	return (c == '|'
		 || c == '\''
		 || c == '"'
		 || c == '/'
		 || c == '-'
		 || c == '_'
		 || c == '$'
		 || c == '?');
}

int is_redirection(char c)
{
	return (c == '<'
		 || c == '>');
}
