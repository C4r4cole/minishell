#include "../minishell.h"

void	print_banner(void)
{
	const char *lines[] = {
		"                                                                                                 ",
		"  ▄▄▄▄███▄▄▄▄    ▄█  ███▄▄▄▄    ▄█     ▄████████    ▄█    █▄       ▄████████  ▄█        ▄█       ",
		"▄██▀▀▀███▀▀▀██▄ ███  ███▀▀▀██▄ ███    ███    ███   ███    ███     ███    ███ ███       ███       ",
		"███   ███   ███ ███▌ ███   ███ ███▌   ███    █▀    ███    ███     ███    █▀  ███       ███       ",
		"███   ███   ███ ███▌ ███   ███ ███▌   ███         ▄███▄▄▄▄███▄▄  ▄███▄▄▄     ███       ███       ",
		"███   ███   ███ ███▌ ███   ███ ███▌ ▀███████████ ▀▀███▀▀▀▀███▀  ▀▀███▀▀▀     ███       ███       ",
		"███   ███   ███ ███  ███   ███ ███           ███   ███    ███     ███    █▄  ███       ███       ",
		"███   ███   ███ ███  ███   ███ ███     ▄█    ███   ███    ███     ███    ███ ███▌    ▄ ███▌    ▄ ",
		" ▀█   ███   █▀  █▀    ▀█   █▀  █▀    ▄████████▀    ███    █▀      ██████████ █████▄▄██ █████▄▄██ ",
		"                                                                             ▀         ▀         ",
		"                                                                                                 ",
		"             福 龙 & Ilyes                                                                        "
	};

	int start_r = 255, start_g = 0, start_b = 0;     // Rouge vif
	int end_r   = 255, end_g   = 0, end_b   = 255;   // Magenta
	int steps = sizeof(lines) / sizeof(lines[0]);    // Nombre de lignes

	for (int i = 0; i < steps; i++)
	{
		// Interpolation linéaire (du rouge au magenta)
		int r = start_r + (end_r - start_r) * i / (steps - 1);
		int g = start_g + (end_g - start_g) * i / (steps - 1);
		int b = start_b + (end_b - start_b) * i / (steps - 1);

		printf("\e[38;2;%d;%d;%dm%s\e[0m\n", r, g, b, lines[i]);
	}
}
